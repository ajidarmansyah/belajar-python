# mengambil karakter string menggunakan index
a = "Hello, World"
print(a[0:5])

# jika tidak mendeklarasikan nilai awal maka index akan dimulai diawal kata
print(a[:5])
# jika tidak mendeklarasikan nilai akhir maka index akhir akan berada diakhir kata
print(a[7:])
# nilai min akan mengambil dari akhir kata
print(a[-5:])
